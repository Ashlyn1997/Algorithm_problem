function ipToInt(ip) {
    let res
    let splited = ip.split(':')
    if (splited.length <= 8 && splited.indexOf('') != -1) {
        let index = splited.indexOf('')
        let temp = splited.slice(index + 1, splited.length)
        let zero = []
        for (let i = 0; i <= 8-splited.length; i++) {
            zero.push('0000')
        }
        splited = splited.slice(0, index).concat(zero).concat(temp)
    }
    res = splited.reduce((acc, item)=> {
        return acc + parseInt(item, 16).toString()
    }, '')
    return res
}
